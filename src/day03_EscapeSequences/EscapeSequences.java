package day03_EscapeSequences;
/*
Escape sequences: MUST be given with double quote
    \n: starts a new line
    \t: paragraph space (tab)  button
    \\: one back slash
    \": double quote

 */

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("--------------------------------");

        System.out.println("Hello Cydeo \nHow are you today? \nit's nice to see you all! \nWhat class do we have next weeek?");

        System.out.println("-----------------------------------");

        System.out.println("\tJava is cool programming language");

        System.out.println("-----------------------------");

        System.out.println("/ \\");// in order to print one backslah we need to give two backward slash

        System.out.println("-----------------------");

        System.out.println("My favorite TV show is \"Game of Throne\"");
        // in order to print double queto, we need to give \"


    }





}


