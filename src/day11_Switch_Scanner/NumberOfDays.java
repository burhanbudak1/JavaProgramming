package day11_Switch_Scanner;

public class NumberOfDays {
    public static void main(String[] args) {
             int number = 2;
             String result = ";";

             switch (number){
                 case 2:
                     result = "28 days";
                     break;

                 case 4:
                     result = "30 days";
                     break;
                 case 6:
                     result = "30 days";
                 case 9:
                     result ="30 days";
                     break;
                 case 11:
                     result="30 days";
                     break;
                 default:
                     result= "31 days";

             }
        System.out.println(result);



















    }



}
